const navLinks = [
    { name: "Inicio", href: "#home" },
    { name: "Acerca de mí", href: "#about" },
    { name: "Habilidades", href: "#skills" },
    { name: "Proyectos", href: "#projects" },
    { name: "Contacto", href: "#contact" },
];

export default function Navbar() {
    const [isScrolled, setIsScrolled] = useState(false);
    const [isOpen, setIsOpen] = useState(false);
    const [mounted, setMounted] = useState(false);

    useEffect(() => {
        setMounted(true);
        const handleScroll = () => setIsScrolled(window.scrollY > 20);
        window.addEventListener("scroll", handleScroll);
        return () => window.removeEventListener("scroll", handleScroll);
    }, []);

    if (!mounted) return null;

    return (
        <nav className={`fixed top-0 left-0 right-0 z-50 transition-all duration-300 ${isScrolled ? "bg-slate-950/90 backdrop-blur-md border-b border-slate-800 py-4" : "bg-transparent py-6"
            }`}>
            <div className="container mx-auto px-6 flex items-center justify-between">
                <Link href="#home" className="flex items-center gap-2 group">
                    <Code2 className="text-indigo-500 group-hover:rotate-12 transition-transform" />
                    <span className="text-white font-bold text-xl tracking-tighter">Luis<span className="text-indigo-500">.dev</span></span>
                </Link>

                {/* Desktop Menu */}
                <div className="hidden md:flex items-center gap-8">
                    {navLinks.map((link) => (
                        <Link
                            key={link.name}
                            href={link.href}
                            className="text-slate-400 text-sm font-medium hover:text-white transition-colors"
                        >
                            {link.name}
                        </Link>
                    ))}
                    <div className="flex gap-4 border-l border-slate-800 pl-8">
                        <a href="https://github.com/eligio12" target="_blank" className="text-slate-400 hover:text-indigo-500 transition-colors"><GithubIcon /></a>
                        <a href="https://linkedin.com/in/luis-eligio-cortes-rodarte-713900295/" target="_blank" className="text-slate-400 hover:text-indigo-500 transition-colors"><LinkedinIcon /></a>
                    </div>
                </div>

                {/* Mobile Toggle */}
                <button className="md:hidden text-white" onClick={() => setIsOpen(!isOpen)}>
                    {isOpen ? <X /> : <Menu />}
                </button>
            </div>

            {/* Mobile Menu */}
            <AnimatePresence>
                {isOpen && (
                    <motion.div
                        initial={{ opacity: 0, height: 0 }}
                        animate={{ opacity: 1, height: "auto" }}
                        exit={{ opacity: 0, height: 0 }}
                        className="md:hidden bg-slate-950 border-b border-slate-800"
                    >
                        <div className="flex flex-col p-6 gap-4">
                            {navLinks.map((link) => (
                                <Link
                                    key={link.name}
                                    href={link.href}
                                    onClick={() => setIsOpen(false)}
                                    className="text-slate-400 hover:text-white text-lg"
                                >
                                    {link.name}
                                </Link>
                            ))}
                        </div>
                    </motion.div>
                )}
            </AnimatePresence>
        </nav>
    );
}